class Solution{
  public:
    int MissingNumber(vector<int>& arr, int n) {
        // Your code goes here
        
        int sum=0;
        for(int el:arr)sum+=el;
        
        long long int orgSum=(n*(n+1))/2;
        
        int ans=orgSum-sum;
        return ans;
    }
};

